CREATE TABLE TB_M_GIM_H (
	GIM_TYPE VARCHAR(50) NOT NULL ,
	GIM_DESC VARCHAR(200) NOT NULL ,
	CD_LENGTH TINYINT NOT NULL,
	FIELD1_LABEL VARCHAR(50) NOT NULL ,
	FIELD2_LABEL VARCHAR(50) NOT NULL ,
	FIELD3_LABEL VARCHAR(50) NOT NULL ,
	ACTIVE_FLAG CHAR(1) NOT NULL ,
	CREATED_BY VARCHAR(10) NOT NULL ,
	CREATED_DT DATETIME NOT NULL,
	MODIFIED_BY VARCHAR(10) NOT NULL ,
	MODIFIED_DT DATETIME NOT NULL,
	PRIMARY KEY (GIM_TYPE)
);

CREATE TABLE TB_M_GIM_D (
	GIM_TYPE VARCHAR(50) NOT NULL ,
	GIM_CD VARCHAR(500) NOT NULL ,
	GIM_VALUE VARCHAR(2000) NOT NULL ,
	FIELD1 VARCHAR(2000) NULL ,
	FIELD2 VARCHAR(2000) NULL ,
	FIELD3 VARCHAR(2000) NULL ,
	ACTIVE_FLAG CHAR(1) NOT NULL ,
	CREATED_BY VARCHAR(10) NOT NULL ,
	CREATED_DT DATETIME NOT NULL,
	MODIFIED_BY VARCHAR(10) NOT NULL ,
	MODIFIED_DT DATETIME NOT NULL,
	PRIMARY KEY (GIM_TYPE, GIM_CD)
)
